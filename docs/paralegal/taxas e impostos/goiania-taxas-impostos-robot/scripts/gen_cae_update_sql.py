def normalize_expanded(s: str) -> str:
    """Normaliza e expande abreviações para comparar nome do dash com o da prefeitura."""
    n = normalize(s)
    for pattern, replacement in ABREV_EXPAND:
        n = re.sub(pattern, replacement, n, flags=re.I)
    return re.sub(r"\s+", " ", n).strip()


def main() -> None:
    # Parse prefeitura: (cae, nome) e índice por nome normalizado
    pref_re = re.compile(r'CAE\s*:\s*(\d+)\s*-\s*([^<]+)', re.I)
    pref_by_norm: dict[str, str] = {}  # norm_name -> cae (primeiro vence se duplicata)
    pref_entries: list[tuple[str, str, str]] = []  # (norm, cae, nome_original)
    for line in PREFEITURA_OPTIONS.strip().split("\n"):
        m = pref_re.search(line)
        if m:
            cae_num, name = m.group(1), m.group(2).strip()
            name = name.replace("&amp;", "&")
            norm = normalize(name)
            if norm and norm not in pref_by_norm:
                pref_by_norm[norm] = cae_num
                pref_entries.append((norm, cae_num, name))

    # Parse dash: linhas alternadas nome, documento (14 dígitos)
    lines = [ln.strip() for ln in DASH_BLOCK.strip().split("\n") if ln.strip()]
    dash: list[tuple[str, str]] = []
    for i in range(0, len(lines) - 1, 2):
        name_line = lines[i]
        doc_line = lines[i + 1]
        digits_only = re.sub(r"\D", "", doc_line)
        if len(digits_only) == 14:
            dash.append((name_line, digits_only))

    # 1) Match exato (nome normalizado do dash == prefeitura)
    pairs: list[tuple[str, str]] = []
    paired_docs: set[str] = set()
    for dash_name, doc in dash:
        norm_dash = normalize(dash_name)
        if norm_dash and norm_dash in pref_by_norm:
            pairs.append((doc, pref_by_norm[norm_dash]))
            paired_docs.add(doc)
    # 2) Match com expansão de abreviações (DISTRIB->DISTRIBUIDORA, NEG->NEGOCIOS, etc.)
    for dash_name, doc in dash:
        if doc in paired_docs:
            continue
        expanded = normalize_expanded(dash_name)
        if expanded and expanded in pref_by_norm:
            pairs.append((doc, pref_by_norm[expanded]))
            paired_docs.add(doc)
    # 3) Mapeamentos manuais (casos que ainda não batem)
    for doc, cae in MANUAL_OVERRIDES:
        if doc not in paired_docs:
            pairs.append((doc, cae))
            paired_docs.add(doc)

    dash_docs = [doc for _, doc in dash]
    # Gera SQL: primeiro zera CAE de todas as empresas do dash; depois preenche só os matches exatos
    print("-- Corrige CAE: zera nas empresas do dash e preenche apenas onde o nome bate EXATAMENTE (apos normalizar).")
    print("-- Execute apos aplicar a migration que adiciona companies.cae.")
    print("")
    print("-- 1) Zera CAE de todas as empresas cujo documento esta na lista do dash (remove valores errados)")
    print("WITH dash_docs(doc_digits) AS (")
    print("  VALUES")
    for i, doc in enumerate(dash_docs):
        comma = "," if i < len(dash_docs) - 1 else ""
        print(f"    ('{doc}'){comma}")
    print(")")
    print("UPDATE companies c")
    print("SET cae = NULL")
    print("FROM dash_docs d")
    print("WHERE regexp_replace(COALESCE(c.document, ''), '\\D', '', 'g') = d.doc_digits;")
    print("")
    print("-- 2) Preenche CAE apenas para empresas com nome exatamente igual ao da prefeitura")
    if pairs:
        print("WITH pairs(doc_digits, cae) AS (")
        print("  VALUES")
        for i, (doc, cae) in enumerate(pairs):
            comma = "," if i < len(pairs) - 1 else ""
            print(f"    ('{doc}', '{cae}'){comma}")
        print(")")
        print("UPDATE companies c")
        print("SET cae = p.cae")
        print("FROM pairs p")
        print("WHERE regexp_replace(COALESCE(c.document, ''), '\\D', '', 'g') = p.doc_digits;")
    else:
        print("-- Nenhum par exato encontrado; pule este bloco.")

    # Relatar empresas do dash ainda sem match (para análise manual)
    unmatched = [(name, doc) for name, doc in dash if doc not in paired_docs]
    if unmatched:
        import sys
        sys.stderr.write(f"\n--- {len(unmatched)} empresa(s) do dash SEM MATCH (exato nem com abreviacoes): ---\n")
        for dash_name, doc in unmatched:
            expanded = normalize_expanded(dash_name)
            # Sugerir candidatos: prefeitura cujo nome contém o início do dash ou vice-versa
            candidates: list[tuple[str, str, str]] = []
            dash_words = set(expanded.split()) - {"E", "DE", "DA", "DO", "DOS", "DAS"}
            for pnorm, cae, pname in pref_entries:
                pwords = set(pnorm.split()) - {"E", "DE", "DA", "DO", "DOS", "DAS"}
                common = len(dash_words & pwords)
                if common >= 3 and (expanded in pnorm or pnorm in expanded or common >= 4):
                    candidates.append((cae, pname, pnorm))
            sys.stderr.write(f"  {doc} | {dash_name}\n")
            if candidates:
                best = candidates[0] if len(candidates) == 1 else sorted(candidates, key=lambda x: -len(x[1]))[0]
                sys.stderr.write(f"    -> Sugestão: CAE {best[0]} | {best[1][:60]}...\n")
            sys.stderr.write("\n")


if __name__ == "__main__":
    main()
