def normalize(s: str) -> str:
    """Normaliza nome para comparação exata: sem acentos, sem pontuação, sufixos removidos."""
    if not s:
        return ""
    s = unicodedata.normalize("NFD", s)
    s = "".join(c for c in s if unicodedata.category(c) != "Mn")  # remove acentos
    s = s.upper().strip()
    s = s.replace("&", " E ").replace(",", " ").replace(".", " ")
    s = re.sub(r"\s+", " ", s).strip()
    for suffix in (
        " LTDA", " ME", " EPP", " EIRELI",
        " - ME", " -ME", " - EPP", " - EIRELI",
    ):
        while s.endswith(suffix):
            s = s[: -len(suffix)].strip()
    s = re.sub(r"\s+", " ", s).strip()
    return s


